"""
Supabase writer — pushes ATS signals/orders/portfolio to Supabase.

Uses the REST API directly (no SDK dependency):
  POST /rest/v1/{table}  — insert rows
  PATCH /rest/v1/{table} — update existing rows

Secrets read from macOS Keychain: ats-supabase (service_role key).
"""

