# ── Keychain helper ──────────────────────────────────────────────────────────


def _get_keychain(service: str = "ats-supabase", account: str = "service_role") -> str:
    """Fetch a secret from macOS Keychain."""
    result = subprocess.run(
        ["security", "find-generic-password", "-w", "-s", service, "-a", account],
        capture_output=True, text=True, check=True,
    )
    return result.stdout.strip()


# ── Supabase REST helpers ────────────────────────────────────────────────────


def _headers() -> dict[str, str]:
    key = _get_keychain()
    return {
        "apikey": key,
        "Authorization": f"Bearer {key}",
        "Content-Type": "application/json",
        "Accept": "application/json",
    }


def _sanitize(obj):
    """Recursively replace NaN/Inf with None (JSON can't serialize them)."""
    if isinstance(obj, dict):
        return {k: _sanitize(v) for k, v in obj.items()}
    if isinstance(obj, list):
        return [_sanitize(v) for v in obj]
    if isinstance(obj, float) and (math.isnan(obj) or math.isinf(obj)):
        return None
    return obj


def _insert(table: str, rows: list[dict]) -> list[dict]:
    """
    Insert rows into a Supabase table (id auto-generated by sequence).
    Returns the response data.
    """
    if not rows:
        return []

    rows = _sanitize(rows)
    url = f"{REST_URL}/{table}"
    resp = requests.post(
        url,
        headers={**_headers(), "Prefer": "return=representation"},
        json=rows,
        timeout=15,
    )
    resp.raise_for_status()
    return resp.json()


def _select(table: str, query: Optional[dict] = None, order: Optional[str] = None, limit: int = 100) -> list[dict]:
    """Select rows from a table."""
    url = f"{REST_URL}/{table}"
    params = {"limit": limit}
    if query:
        for k, v in query.items():
            params[k] = f"eq.{v}"
    if order:
        params["order"] = order
    resp = requests.get(url, headers=_headers(), params=params, timeout=15)
    resp.raise_for_status()
    return resp.json()


